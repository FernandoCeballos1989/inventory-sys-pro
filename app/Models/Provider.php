{
    use HasFactory;

    protected $fillable = [
        'company_name',
        'contact_name',
        'phone_number',
        'email',
        'address',
    ];

    /**
     * RelaciÃ³n: Un proveedor puede suministrar muchos movimientos de stock.
     */
    public function stocks(): HasMany
    {
        return $this->hasMany(Stock::class);
    }
}
